
public class Horse
{
  private String name;
  private int weight;

  public Horse (String n, int w)
  {
    name = n;
    weight = w;
  }

  String getName()
  { 
    return name;
  }
  
  int getWeight()
  {
    return weight;
  }

  public String toString()
  {
    return name;
  }
}
